package com.sandbox_maven.test02.jpa.springDataJpa.repository;

import com.sandbox_maven.test02.jpa.crud.entities.Album;
import com.sandbox_maven.test02.jpa.crud.entities.Singer;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.data.jpa.repository.Query;
import org.springframework.data.repository.query.Param;
import org.springframework.stereotype.Repository;

import java.util.List;

@Repository("albumRepository")
public interface AlbumRepository extends JpaRepository<Album, Long> {
    List<Album> findBySinger(Singer singer);

    @Query("select a from Album a where a.title like %:title%")
    List<Album> findByTitle(@Param("title") String title);
}